"""Create Variables using formats such as Upper CamelCase, Lower CamelCase,
 SnakeCase and UPPERCASE."""

#Upper CamelCase
MyName = "Pratibha"

#Lower CamelCase
myName = "Kunal"

#SnakeCase
my_name = "Myra"

#UPPERCASE
MYNAME = "Syna"

print ("The values of Upper Camel, Lower Camel, Snake, Upper Cases Variables are:\n {}, {}, {}, {}"
.format(MyName, myName, my_name, MYNAME, sep = ","))