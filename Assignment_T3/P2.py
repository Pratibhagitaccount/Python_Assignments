#Create a list of size 5 and execute the slicing structure

#Creation of list_1 with 5 int elements
list_1 = [1,5,3,6,2]
#slicing first three elements of the list
print(list_1[:3])
#slicing last three elements of the list
print(list_1[-3:])
#slicing 2nd, third and fourth elements of the list
print(list_1[1:4])


